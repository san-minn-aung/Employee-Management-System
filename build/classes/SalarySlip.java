    private void fetch(String _id, String netSalary) throws SQLException{
        try{
    
            stmt = conn.createStatement();
            JOptionPane.showMessageDialog(rootPane, _id);
            sql="select * from Employee where ID = '"+_id+"'";
            rs = stmt.executeQuery(sql); 
             while(rs.next()){
                 String name=rs.getString("Name");
                 String nrcno=rs.getString("NRC_NO");
                 String phone=rs.getString("Telephone");
                 String department=rs.getString("Department");
                 String salary=rs.getString("Basic_Salary");
                 lblID.setText(_id);
                 lblName.setText(name);
                 lblNRC.setText(nrcno);
                 lblDept.setText(department);
                 lblPhone.setText(phone);
                 lblBasic.setText(salary); 
                 lblNetSalary.setText(netSalary);
             }
             
        }catch(SQLException ex){
            
        }
    }
    
    
